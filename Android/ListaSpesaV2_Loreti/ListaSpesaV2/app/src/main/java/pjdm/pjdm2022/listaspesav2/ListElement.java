package pjdm.pjdm2022.listaspesav2;

public class ListElement {
    private String name;
    private int num;

    public ListElement(String name, int num) {
        this.name = name;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public int getNum() {
        return num;
    }
}
